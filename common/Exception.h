//==============================================================================
// Exceptionクラスヘッダ
//==============================================================================
#ifndef _EXCEPTION_H_                       // 二重インクルード防止
#define _EXCEPTION_H_                       // 二重インクルード防止

//==============================================================================
// インクルードファイル
//==============================================================================
#include <string.h>
#include <errno.h>
#include <string>
#include <iostream>
#include <sstream>

//==============================================================================
// クラス定義
//==============================================================================
//------------------------------------------------------------------------------
// Exceptionクラス
//------------------------------------------------------------------------------
class Exception
{
public:
    std::string Msg;                        // メッセージ

public:
    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    Exception(std::string msg)
    {
        // メッセージ設定
        this->Msg = msg;
    };

    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    Exception(int no)
    {
        // メッセージ設定
        this->Msg = strerror(no);
    };

    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    Exception(std::string msg, int no)
    {
        std::stringstream _errmsg;

        // メッセージ作成
        _errmsg << msg << ":[" << strerror(no) << "]\n";

        // メッセージ設定
        this->Msg = _errmsg.str();
    };
};
#endif                                      // 二重インクルード防止
