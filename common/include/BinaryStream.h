//==============================================================================
// BinaryStreamクラスヘッダ
//==============================================================================
#ifndef _BINARY_STREAM_H_                   // 二重インクルード防止
#define _BINARY_STREAM_H_                   // 二重インクルード防止

//==============================================================================
// インクルードファイル
//==============================================================================
#include "MemoryStream.h"

//==============================================================================
// クラス定義
//==============================================================================
//------------------------------------------------------------------------------
// BinaryStream例外クラス
//------------------------------------------------------------------------------
class BinaryStreamException : public Exception
{
public:
    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    BinaryStreamException(std::string format, ...)
        : Exception()
    {
        // メッセージ生成
        va_list ap;
        va_start(ap, format);
        this->SetMessage(format, ap);
        va_end(ap);
    };
};

//------------------------------------------------------------------------------
// BinaryStreamクラス
//------------------------------------------------------------------------------
class BinaryStream : public MemoryStream
{
public:
    //-----------------------------------------------------------------------------
    // コンストラクタ
    //-----------------------------------------------------------------------------
    BinaryStream() : MemoryStream()
    {
    }

    //-----------------------------------------------------------------------------
    // コンストラクタ
    //-----------------------------------------------------------------------------
    BinaryStream(size_t block_size) : MemoryStream(block_size)
    {
    }

    //-----------------------------------------------------------------------------
    // コンストラクタ
    //-----------------------------------------------------------------------------
    BinaryStream(size_t block_size, size_t block_count) : MemoryStream(block_size, block_count)
    {
    }

    //-----------------------------------------------------------------------------
    // コンストラクタ
    //-----------------------------------------------------------------------------
    BinaryStream(const BinaryStream& stream) : MemoryStream(stream)
    {
    }

    //-----------------------------------------------------------------------------
    // デストラクタ
    //-----------------------------------------------------------------------------
    virtual ~BinaryStream()
    {
    }
};
#endif                                      // 二重インクルード防止
