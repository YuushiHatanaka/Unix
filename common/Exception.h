//==============================================================================
// Exceptionクラスヘッダ
//==============================================================================
#ifndef _EXCEPTION_H_                       // 二重インクルード防止
#define _EXCEPTION_H_                       // 二重インクルード防止

//==============================================================================
// インクルードファイル
//==============================================================================
#include <stdio.h>
#include <string.h>
#include <string>

//==============================================================================
// クラス定義
//==============================================================================
//------------------------------------------------------------------------------
// Exceptionクラス
//------------------------------------------------------------------------------
class Exception
{
public:
    std::string Msg;                        // メッセージ

public:
    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    Exception(std::string msg)
    {
        // メッセージ設定
        this->Msg = msg;
    };

    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    Exception(int no)
    {
        // メッセージ設定
        this->Msg = strerror(no);
    };
};
#endif                                      // 二重インクルード防止
