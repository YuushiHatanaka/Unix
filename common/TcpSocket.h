#ifndef _TCP_SOCKET_H_                      // 二重インクルード防止
#define _TCP_SOCKET_H_                      // 二重インクルード防止

//==============================================================================
// インクルードファイル
//==============================================================================
#include "Exception.h"
#include "Socket.h"

//==============================================================================
// クラス定義
//==============================================================================
//------------------------------------------------------------------------------
// TcpSocket例外クラス
//------------------------------------------------------------------------------
class TcpSocketException : public Exception
{
public:
    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    TcpSocketException(std::string msg) : Exception(msg)
    {
    };
};

//------------------------------------------------------------------------------
// TcpSocketクラス
//------------------------------------------------------------------------------
class TcpSocket : public Socket
{
public:
    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    TcpSocket() : Socket()
    {
    }

    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    TcpSocket(int socket) : Socket(socket)
    {
    }

    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    TcpSocket(int domain, int protocol) : Socket()
    {
        // 生成
        this->Create(domain, protocol);
    }

    //--------------------------------------------------------------------------
    // コピーコンストラクタ
    //--------------------------------------------------------------------------
    TcpSocket(TcpSocket& socket) : Socket(socket)
    {
    }

    //--------------------------------------------------------------------------
    // デストラクタ
    //--------------------------------------------------------------------------
    virtual ~TcpSocket()
    {
    }

    //--------------------------------------------------------------------------
    // 生成
    //--------------------------------------------------------------------------
    bool Create(int domain, int protocol)
    {
        // 親クラスのCreateメソッドを呼び出し、結果を返却
        return Socket::Create(domain, SOCK_STREAM, protocol);
    }
};
#endif                                      // 二重インクルード防止
